			<section class="sec02">
				<h3 class="aboutTtl">不動産クラウドファンディングの仕組み</h3>
				<div class="secBody">
					<p class="txt">
						不動産投資型クラウドファンディングでは、事業者が一般投資家から出資を募り運営を行います。投資家は投資したいファンドを選び入金の手続きを行うだけで分配金を受け取ることができます。平均的な利回りは2～8％と言われており、運用期間は提供するサービスによっても異なります。手続きはすべてインターネット上で行われるため、気軽に始めることができるのも特徴の1つです。<br>
						投資物件には様々な種類があり、居住用のマンション、商業ビルだけでなく、リゾート地のホテルや空き家のリノベーションなどまちづくりに貢献できる物件もあります。
					</p>
