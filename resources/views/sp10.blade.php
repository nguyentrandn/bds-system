						<h3>不動産クラウドファンディング<br class="sp">の3つの特徴的なポイント</h3>
						<div class="txt">
							<dl>
								<dt>運用の手間が少ない</dt>
								<dd>物件の修繕や入居者の管理のような管理業務はすべて事業者が行います。投資家は入金し分配金を待つだけのため、手間なくお金の運用をすることができます。</dd>
